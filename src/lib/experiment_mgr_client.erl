-module(experiment_mgr_client).

%% API
-export([
  start_run/0,
  run_complete/2
]).

%% Xref
-ignore_xref([
  start_run/0,
  run_complete/2
]).

%%%============================================================================
%%% API
%%%============================================================================

%%-----------------------------------------------------------------------------
%% @doc start_run message.
%% @end
%%-----------------------------------------------------------------------------
-spec start_run() -> ok.
start_run() ->
  experiment_mgr:run(),
  ok.

%%-----------------------------------------------------------------------------
%% @doc run_complete message.
%% @end
%%-----------------------------------------------------------------------------
-spec run_complete(models:population_id(), models:trace()) -> ok.
run_complete(PopulationId, Trace) ->
  experiment_mgr:complete(PopulationId, Trace),
  ok.

